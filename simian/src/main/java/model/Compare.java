package model;
/**
 * @author y-yusuke
 *
 */
public class Compare {
	private int index;
	private double min;
	private double max;

	public Compare (){
	}

	public int getIndex() {
		return index;
	}

	public void setIndex(int index) {
		this.index = index;
	}

	public double getMin() {
		return min;
	}

	public void setMin(double min) {
		this.min = min;
	}

	public double getMax() {
		return max;
	}

	public void setMax(double max) {
		this.max = max;
	}
}
