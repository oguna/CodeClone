select CODE_FRAGMENT_GENEALOGY_ELEMENT.CODE_FRAGMENT_ID,CODE_FRAGMENT_GENEALOGY_ELEMENT.CODE_FRAGMENT_GENEALOGY_ID,
		CRD.TYPE,REPOSITORY.REPOSITORY_ROOT_URL,FILE.FILE_PATH,CODE_FRAGMENT.START_LINE,CODE_FRAGMENT.END_LINE,REVISION.REVISION_IDENTIFIER,
		CODE_FRAGMENT_GENEALOGY.START_COMBINED_REVISION_ID,CODE_FRAGMENT_GENEALOGY.END_COMBINED_REVISION_ID,
		CODE_FRAGMENT.START_COMBINED_REVISION_ID,CODE_FRAGMENT.END_COMBINED_REVISION_ID
from ((((((
	CODE_FRAGMENT_GENEALOGY INNER JOIN CODE_FRAGMENT_GENEALOGY_ELEMENT
	ON CODE_FRAGMENT_GENEALOGY.CODE_FRAGMENT_GENEALOGY_ID = CODE_FRAGMENT_GENEALOGY_ELEMENT.CODE_FRAGMENT_GENEALOGY_ID 
	)INNER JOIN CODE_FRAGMENT
	ON CODE_FRAGMENT_GENEALOGY_ELEMENT.CODE_FRAGMENT_ID = CODE_FRAGMENT.CODE_FRAGMENT_ID
	)INNER JOIN CRD
	ON CODE_FRAGMENT.CRD_ID = CRD.CRD_ID
	)INNER JOIN FILE
	ON CODE_FRAGMENT.OWNER_FILE_ID = FILE.FILE_ID
	)INNER JOIN REPOSITORY
	ON CODE_FRAGMENT.OWNER_REPOSITORY_ID = REPOSITORY.REPOSITORY_ID
	)INNER JOIN REVISION
	ON CODE_FRAGMENT.END_COMBINED_REVISION_ID = REVISION.REVISION_ID
)
where CODE_FRAGMENT_GENEALOGY.END_COMBINED_REVISION_ID = 757 AND CODE_FRAGMENT.END_COMBINED_REVISION_ID = 757