select CANDIDATE.*,REPOSITORY.REPOSITORY_ROOT_URL,FILE.FILE_PATH,REVISION.REVISION_IDENTIFIER,
		CODE_FRAGMENT.START_LINE,CODE_FRAGMENT.END_LINE
from ((((
	CANDIDATE INNER JOIN CODE_FRAGMENT
	ON CANDIDATE.CODE_FRAGMENT_ID = CODE_FRAGMENT.CODE_FRAGMENT_ID
	)INNER JOIN FILE
	ON CODE_FRAGMENT.OWNER_FILE_ID = FILE.FILE_ID
	)INNER JOIN REPOSITORY
	ON CODE_FRAGMENT.OWNER_REPOSITORY_ID = REPOSITORY.REPOSITORY_ID
	)INNER JOIN REVISION
	ON CANDIDATE.REVISION = REVISION.REVISION_ID
)where (CANDIDATE.REVISION = 121 AND CANDIDATE.PROCESS = "after_fix" ) OR (CANDIDATE.REVISION = 121 AND CANDIDATE.PROCESS = "add")
OR (CANDIDATE.REVISION = 121 AND CANDIDATE.PROCESS = "before_fix") OR (CANDIDATE.REVISION = 121 AND CANDIDATE.PROCESS = "delete" )